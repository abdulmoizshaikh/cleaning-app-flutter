class CreditCard {
  String number = "", holderName = "", expiryDate = "", cvv = "";

  void setNumber(String number) {
    this.number = number;
  }

  void setHolderName(String number) {
    this.holderName = holderName;
  }

  void setExpiryDate(String number) {
    this.expiryDate = expiryDate;
  }

  void setCVV(String number) {
    this.cvv = cvv;
  }
}
